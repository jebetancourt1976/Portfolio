def _format_docs(docs):
    return "\n\n---\n\n".join(d.page_content for d in docs)


class PolicyAgent:
    """A single-policy RAG specialist. Returns either a grounded answer or 'NOT_FOUND'."""

    def __init__(self, name: str, vectorstore: FAISS, llm: ChatOpenAI,
                 k: int = 4, prompt_template: str = STRICT_PROMPT):
        self.name = name
        self.vectorstore = vectorstore
        self.llm = llm
        self.k = k
        self.retriever = vectorstore.as_retriever(search_kwargs={"k": k})
        self._prompt = PromptTemplate(
            input_variables=["context", "question"],
            template=prompt_template.replace("{policy_label}", name),
        )
        self._chain = self._prompt | llm | StrOutputParser()

    def ask(self, question: str) -> dict:
        docs = self.retriever.invoke(question)
        context = _format_docs(docs)
        answer = self._chain.invoke({"context": context, "question": question}).strip()
        found = "NOT_FOUND" not in answer.upper().split()
        sources = [
            {
                "page": d.metadata.get("page"),
                "policy": d.metadata.get("policy", self.name),
                "source_file": d.metadata.get("source_file"),
                "snippet": d.page_content[:220].replace("\n", " ") + "...",
            }
            for d in docs
        ]
        return {"answer": answer, "found": found, "sources": sources}

