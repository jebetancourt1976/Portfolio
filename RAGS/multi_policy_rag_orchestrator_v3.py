"""## 3. Build (or reload) the vector store

This is upgrade **#4 — Persistent FAISS indexes**. The helper first checks whether an index for
this policy already exists on disk; if it does, it reloads it instantly. Only the first run pays
the embedding cost.

> ⚠️ `FAISS.load_local(..., allow_dangerous_deserialization=True)` is required because FAISS
> indexes are stored as pickled Python objects. Only enable this for indexes you built yourself.

"""

def build_or_load_vectorstore(pdf_path: str, label: str, index_path: Path) -> FAISS:
    """Return a FAISS index for the given PDF, loading it from disk if available."""
    embeddings = OpenAIEmbeddings(model=EMBED_MODEL)

    if index_path.exists():
        print(f"[{label}] Reloading existing index from {index_path} ...")
        vs = FAISS.load_local(
            str(index_path),
            embeddings,
            allow_dangerous_deserialization=True,
        )
        print(f"[{label}]   Loaded.\n")
        return vs

    print(f"[{label}] Building new index from {pdf_path} ...")
    #loader = PyPDFLoader(pdf_path)
    #loader = PDFPlumberLoader(pdf_path)
    loader = PyMuPDFLoader(pdf_path)
    pages = loader.load()
    print(f"[{label}]   {len(pages)} pages loaded.")

    splitter = RecursiveCharacterTextSplitter(
        chunk_size=800,
        chunk_overlap=100,
        separators=["\n\n", "\n", ". ", " ", ""],
    )
    chunks = splitter.split_documents(pages)

    # ---- Upgrade #3: Provenance tag on EVERY chunk ----
    for c in chunks:
        c.metadata["policy"] = label
        c.metadata["source_file"] = os.path.basename(pdf_path)
    print(f"[{label}]   {len(chunks)} chunks created, all tagged with policy='{label}'.")

    vs = FAISS.from_documents(chunks, embeddings)
    vs.save_local(str(index_path))
    print(f"[{label}]   Index saved to {index_path}.\n")
    return vs


